local compe = require 'compe'

local Source = {}

local function copytable(orig)
	local copy = {}
	for orig_key, orig_value in pairs(orig) do
		copy[orig_key] = orig_value
	end
	return copy
end

function Source.get_metadata(self)
	return {
		priority = 100,
		dup = 1,
		menu = '[latex]',
	}
end

function Source.determine(self, context)
	local start = context.before_line:find('\\%S*$')
	if start ~= nil then
		return {
			keyword_pattern_offset = start,
			trigger_character_offset = start,
		}
	end
	return {}
end

local symbols
local symbols_1l = nil
local symbols_all = nil

local function prepare_symbols()
	symbols_1l = {}
	symbols_all = {}

	for code, symbol in pairs(symbols) do
		local first_letter = code:sub(2, 2)
		local precompiled = {
			word = symbol,
			filter_text = code,
			menu = code,
		}

		local by_letter = symbols_1l[first_letter]
		if by_letter == nil then
			symbols_1l[first_letter] = {
				[code] = precompiled,
			}
		else
			by_letter[code] = precompiled
		end

		table.insert(symbols_all, precompiled)
	end
end

function Source.complete(self, args)
	local input = args.input
	if input == nil or input == "" then
		return
	end

	if symbols_1l == nil then
		prepare_symbols()
	end

	local first_letter = input:sub(2, 2)
	if first_letter == "" then
		args.callback { items = symbols_all }
		return
	end

	local items = {}
	for code, symbol in pairs(symbols_1l[first_letter]) do
		if code:sub(1, #input):lower() == input:lower() then
			if vim.b.compe_latex_insert_code then
				local symbol_rev = copytable(symbol)
				symbol_rev.word = symbol_rev.menu
				table.insert(items, symbol_rev)
			else
				table.insert(items, symbol)
			end
		end
	end

	args.callback { items = items }
end

